def get_split_square():
    return [
        [0], [1], [2], [3],
        [0, 1], [0, 2], [1, 2], [0, 3], [2, 3],
        [0, 1, 2], [0, 2, 3]
    ]

def get_triangle():
    return [[0],[1],[2],
        [0,1], [0,2], [1,2],
        [0,1,2]]

def get_three_triangles_with_hole():
    return [[0],[1],[2],[3],[4],[5],
            [0,1],[0,3],[1,3],
            [1,2],[1,4],[2,4],
            [3,5],[4,5],[3,4],
            [0,1,3],[1,2,4],[3,4,5]
            ]

